package com.skills.java;

public class Operators {

	public static void main(String[] args) {
		
		int a = 100;
		int b = 50;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		System.out.println(++a);
		System.out.println(++a);
		System.out.println(++a);
		
		System.out.println(--b);
		System.out.println(--b);
		System.out.println(--b);
	}
}
